struct ListNode
{
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

class Solution
{
public:
    ListNode *detectCycle(ListNode *head)
    {
        unordered_map<ListNode *, int> mp;
        ListNode *ptr = head;
        while (ptr)
        {
            if (mp[ptr])
                return ptr;
            mp[ptr]++;
            ptr = ptr->next;
        }
        return nullptr;
    }
};